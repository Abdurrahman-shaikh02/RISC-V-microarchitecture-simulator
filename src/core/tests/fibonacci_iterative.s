// ============================================================
// ITERATIVE FIBONACCI
//
// Compute F(10)
//
// Expected:
// 0x0900 = 55 (0x00000037)
// ============================================================


// ------------------------------------------------------------
// a = 0
// b = 1
// n = 10
// i = 0
// ------------------------------------------------------------

        addi    r1, r0, 0          // a
        addi    r2, r0, 1          // b
        addi    r3, r0, 10         // n
        addi    r4, r0, 0          // i


fib_loop:

        bge     r4, r3, fib_done

        add     r5, r1, r2         // t = a+b

        addi    r1, r2, 0          // a=b
        addi    r2, r5, 0          // b=t

        addi    r4, r4, 1          // i++

        jal     r0, fib_loop


fib_done:


// ------------------------------------------------------------
// Store answer
//
// After 10 iterations:
// a = 55
// b = 89
// ------------------------------------------------------------

        lui     r6, 0x00001
        addi    r6, r6, -1792      // 0x0900

        sw      r1, 0(r6)

done:

        ebreak
