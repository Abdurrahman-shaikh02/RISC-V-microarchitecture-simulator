// ============================================================
// STRING LENGTH TEST
//
// "Hello\0"
//
// Expected:
// 0x0900 = 5
// ============================================================


// ============================================================
// Base address = 0x0800
// ============================================================

        lui     r1, 0x00001
        addi    r1, r1, -2048          // r1 = 0x0800


// ------------------------------------------------------------
// Initialize string
// ------------------------------------------------------------

        addi    r2, r0, 72             // 'H'
        sb      r2, 0(r1)

        addi    r2, r0, 101            // 'e'
        sb      r2, 1(r1)

        addi    r2, r0, 108            // 'l'
        sb      r2, 2(r1)

        addi    r2, r0, 108            // 'l'
        sb      r2, 3(r1)

        addi    r2, r0, 111            // 'o'
        sb      r2, 4(r1)

        addi    r2, r0, 0              // '\0'
        sb      r2, 5(r1)


// ============================================================
// strlen
// ============================================================

        addi    r3, r0, 0              // length = 0
        addi    r4, r1, 0              // ptr = base

strlen_loop:

        lb      r5, 0(r4)

        beq     r5, r0, strlen_done

        addi    r3, r3, 1
        addi    r4, r4, 1

        jal     r0, strlen_loop


strlen_done:


// ============================================================
// Store result at 0x0900
// ============================================================

        lui     r6, 0x00001
        addi    r6, r6, -1792          // 0x0900

        sw      r3, 0(r6)


// ============================================================
// End
// ============================================================

done:

        ebreak
