// ============================================================
// RV32I MEMORY TEST
//
// Tests:
// 0x00000000
// 0xFFFFFFFF
// 0x55555555
// 0xAAAAAAAA
//
// Result:
// 0x0900 = 1 -> PASS
// 0x0900 = 0 -> FAIL
// ============================================================


// ============================================================
// BASE ADDRESS = 0x0800
// RESULT ADDR  = 0x0900
// ============================================================

        lui     r1, 0x00001
        addi    r1, r1, -2048          // r1 = 0x0800

// ------------------------------------------------------------
// Store 0x00000000
// ------------------------------------------------------------

        addi    r2, r0, 0
        sw      r2, 0(r1)

// ------------------------------------------------------------
// Store 0xFFFFFFFF
// ------------------------------------------------------------

        addi    r2, r0, -1
        sw      r2, 4(r1)

// ------------------------------------------------------------
// Store 0x55555555
// ------------------------------------------------------------

        lui     r2, 0x55555
        addi    r2, r2, 1365           // +0x555
        sw      r2, 8(r1)

// ------------------------------------------------------------
// Store 0xAAAAAAAA
// ------------------------------------------------------------

        lui     r2, 0xAAAAB
        addi    r2, r2, -1366          // +0xAAA
        sw      r2, 12(r1)

// ============================================================
// VERIFY 0x00000000
// ============================================================

        lw      r3, 0(r1)
        bne     r3, r0, fail


// ============================================================
// VERIFY 0xFFFFFFFF
// ============================================================

        addi    r2, r0, -1
        lw      r3, 4(r1)
        bne     r3, r2, fail

// ============================================================
// VERIFY 0x55555555
// ============================================================

        lui     r2, 0x55555
        addi    r2, r2, 1365

        lw      r3, 8(r1)

        bne     r3, r2, fail


// ============================================================
// VERIFY 0xAAAAAAAA
// ============================================================

        lui     r2, 0xAAAAB
        addi    r2, r2, -1366

        lw      r3, 12(r1)

        bne     r3, r2, fail


// ============================================================
// PASS
// ============================================================

pass:

        lui     r4, 0x00001
        addi    r4, r4, -1792          // r4 = 0x0900

        addi    r5, r0, 1
        sw      r5, 0(r4)

        jal     r0, done


// ============================================================
// FAIL
// ============================================================

fail:

        lui     r4, 0x00001
        addi    r4, r4, -1792          // r4 = 0x0900

        addi    r5, r0, 0
        sw      r5, 0(r4)


// ============================================================
// END
// ============================================================

done:

        ebreak
	nop
	nop
	nop
	nop

