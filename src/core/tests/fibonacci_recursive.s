// ============================================================
// RECURSIVE FIBONACCI
//
// fib(10) = 55
//
// Result stored at 0x0900
//
// Stack grows downward from 0x0FFC
// ============================================================


// ------------------------------------------------------------
// Initialize SP
// ------------------------------------------------------------

        lui     r2, 0x00001
        addi    r2, r2, -4            // 0x0FFC


// ------------------------------------------------------------
// fib(10)
// ------------------------------------------------------------

        addi    r5, r0, 10

        jal     r1, fib


// ------------------------------------------------------------
// Store result
// ------------------------------------------------------------

        lui     r6, 0x00001
        addi    r6, r6, -1792         // 0x0900

        sw      r5, 0(r6)

        jal     r0, done



// ============================================================
// fib(n)
//
// input : r5
// output: r5
// ============================================================

fib:

// if n < 2 return n

        addi    r7, r0, 2

        blt     r5, r7, fib_return


// ------------------------------------------------------------
// Stack frame
//
// sp+0  : n
// sp+4  : ra
// sp+8  : fib(n-1)
// ------------------------------------------------------------

        addi    r2, r2, -12

        sw      r5, 0(r2)
        sw      r1, 4(r2)


// ------------------------------------------------------------
// fib(n-1)
// ------------------------------------------------------------

        addi    r5, r5, -1

        jal     r1, fib

        sw      r5, 8(r2)


// ------------------------------------------------------------
// fib(n-2)
// ------------------------------------------------------------

        lw      r8, 0(r2)

        addi    r5, r8, -2

        jal     r1, fib


// ------------------------------------------------------------
// result = fib(n-1)+fib(n-2)
// ------------------------------------------------------------

        lw      r9, 8(r2)

        add     r5, r5, r9


// ------------------------------------------------------------
// Restore frame
// ------------------------------------------------------------

        lw      r1, 4(r2)

        addi    r2, r2, 12

        jalr    r1, 0(r1)



fib_return:

        jalr    r1, 0(r1)



done:

        ebreak
        nop
        nop
        nop
        nop
