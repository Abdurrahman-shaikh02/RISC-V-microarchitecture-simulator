void quick_sort(int left, int right)
{
    int i, j;
    unsigned int pivot;
    unsigned int temp;

    if (left >= right)
        return;

    pivot = arr[(left + right) / 2];

    i = left;
    j = right;

    while (i <= j)
    {
        while (arr[i] < pivot)
            i++;

        while (arr[j] > pivot)
            j--;

        if (i <= j)
        {
            temp = arr[i];
            arr[i] = arr[j];
            arr[j] = temp;

            i++;
            j--;
        }
    }

    if (left < j)
        quick_sort(left, j);

    if (i < right)
        quick_sort(i, right);
}


