//ASSUMING THAT WE ARE FREE TO USE MEM LOCATIONS 0x800 to 0xFFF
// ============================================================
// RECURSIVE FACTORIAL
//
// fact(5) = 120
//
// Result:
// 0x0900 = 120
//
// Stack:
// grows downward from 0x0FFC
// ============================================================


// ------------------------------------------------------------
// Initialize stack
// ------------------------------------------------------------

        lui     r2, 0x00001
        addi    r2, r2, -4            // r2 = 0x0FFC


// ------------------------------------------------------------
// n = 5
// ------------------------------------------------------------

        addi    r5, r0, 5

        jal     r1, fact


// ------------------------------------------------------------
// Store result
// ------------------------------------------------------------

        lui     r6, 0x00001
        addi    r6, r6, -1792         // 0x0900

        sw      r5, 0(r6)

        jal     r0, done



// ============================================================
// fact(n)
//
// input : r5
// output: r5
// ============================================================

fact:

// if n < 2 return 1

        addi    r7, r0, 2

        blt     r5, r7, fact_base


// ------------------------------------------------------------
// Push frame
//
// [sp+0] = n
// [sp+4] = ra
// ------------------------------------------------------------

        addi    r2, r2, -8

        sw      r5, 0(r2)
        sw      r1, 4(r2)


// ------------------------------------------------------------
// fact(n-1)
// ------------------------------------------------------------

        addi    r5, r5, -1

        jal     r1, fact


// ------------------------------------------------------------
// Restore n
// ------------------------------------------------------------

        lw      r8, 0(r2)            // original n
        lw      r1, 4(r2)

        addi    r2, r2, 8


// ------------------------------------------------------------
// Multiply
//
// r5 = fact(n-1)
// r8 = n
//
// result = r5 * r8
// ------------------------------------------------------------

        addi    r9,  r0, 0           // result
        addi    r10, r8, 0           // counter


mul_loop:

        beq     r10, r0, mul_done

        add     r9, r9, r5

        addi    r10, r10, -1

        jal     r0, mul_loop


mul_done:

        addi    r5, r9, 0

        jalr    r1, 0(r1)



// ------------------------------------------------------------
// Base case
// ------------------------------------------------------------

fact_base:

        addi    r5, r0, 1

        jalr    r1, 0(r1)



done:

        ebreak
        nop
        nop
        nop
        nop
